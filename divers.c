int GetIsVectorValid(struct vect vVector)
{
    if(vVector.x<0 || vVector.x>9
       || vVector.y<0 || vVector.y>9)
        {return 0;}
    return 1;
}


/*====================================================================================================================================================
   >>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>
  ====================================================================================================================================================*/
void SetVectorInvalid(struct vect *vVector)
{
    (*vVector).x=-1;
    (*vVector).y=-1;
}


