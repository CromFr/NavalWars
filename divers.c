//Random entre A et B inclus
int RandomAB(int a, int b)
{
    return (rand()%(b+1-a) +a);
}


char IntToCharABC(int i)
{
return i+0x41;
}

int CharABCToInt(char c)
{
return (c-0x61)%0x20;
}
